package eu.trentorise.smartcampus.universiadi.model;

public class AnswerObject {

	private String question;
	private String answer;
	private Integer tottag;
	private Integer usefultag;
	
	public String getQuestion() {
		return question;
	}
	public void setQuestion(String question) {
		this.question = question;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	public Integer getTottag() {
		return tottag;
	}
	public void setTottag(Integer tottag) {
		this.tottag = tottag;
	}
	public Integer getUsefultag() {
		return usefultag;
	}
	public void setUsefultag(Integer usefultag) {
		this.usefultag = usefultag;
	}
	
	
}
